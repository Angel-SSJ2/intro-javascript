/**
 *  OPERADORES DE COMPARACION
 */
 
// ==  : igualdad
// === : para comparar igualdad en valor y tipo de dato
// !=  : distinto
// !== : distinto en valor y tipo de dato
 
// >   : mayor que
// <   : menor que
// <=  : menor igual que
// >=  : mayor igual que
 
const a = 10
const b = 20
const c = "10"
 
console.log(a == b)//false
console.log(a === c)//false
console.log(a == b)//false
console.log(a == c)//true
console.log(a != c)//false -> si se interpreta como iguales
console.log(a !== c)//true
 